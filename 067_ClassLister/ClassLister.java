public class ClassLister {
    public static void main(String[] args)
    {
        //creating array
        String[] classes = {"Calculus", "Gym", "Band", "Physics", "Spectrum", "APCS"};
        //creating for loop
        for(int i = 0; i < classes.length; i++)
        {
            System.out.println(classes[i]);
        }
    }
    
}

